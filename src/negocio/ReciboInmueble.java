
package negocio;

public class ReciboInmueble {
	
	private String FechaPago;
	private float Importe;
	
	
	public String getFechaPago() {
		return FechaPago;
	}
	public void setFechaPago(String fechaPago) {
		FechaPago = fechaPago;
	}
	public float getImporte() {
		return Importe;
	}
	public void setImporte(float importe) {
		Importe = importe;
	}
	
}
